start=`date +%s`
#-------------------------------------------------------------------------------------#

export -p IDIR=/application
echo $IDIR
export -p IXDIR=$IDIR/processing_block_p/bin/

#-------------------------------------------------------------------------------------# 
#-------------------------------------------------------------------------------------# 
function igcx(){
